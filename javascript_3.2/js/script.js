function multiply() {
    firstInputNumber = document.getElementById("firstNumber").value;
    secondInputNumber = document.getElementById("secondNumber").value;
    document.getElementById("result").innerHTML = firstInputNumber * secondInputNumber;
}
function divide() {
    firstInputNumber = document.getElementById("firstNumber").value;
    secondInputNumber = document.getElementById("secondNumber").value;
    document.getElementById("result").innerHTML = firstInputNumber / secondInputNumber;
}